function getValue(object, key) {
  return key.split('.').reduce((acc, part) => (acc == null ? undefined : acc[part]), object)
}

export function translate(language, key, params = {}) {
  const template =
    getValue(translations[language], key) ??
    getValue(translations.en, key) ??
    getValue(translations[DEFAULT_LANGUAGE], key) ??
    key

  return Object.entries(params).reduce(
    (result, [param, value]) => result.replaceAll(`{{${param}}}`, String(value)),
    template
  )
}

export function getDocumentLanguageName(language) {
  return LANGUAGE_NAMES[language] ?? LANGUAGE_NAMES[DEFAULT_LANGUAGE]
}
